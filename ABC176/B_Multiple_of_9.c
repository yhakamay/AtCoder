#include <stdio.h>

int		main(void) {
	int		i;
	char	s[200000];
	long	sum;

	scanf("%s", s);
	for (i = 0, sum = 0; s[i]; i++)
		sum += s[i] - 48;
	puts(sum % 9 == 0 ? "Yes" : "No");
	return 0;
}
